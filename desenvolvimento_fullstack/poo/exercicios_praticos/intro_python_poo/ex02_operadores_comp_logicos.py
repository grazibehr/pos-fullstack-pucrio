a = 10
b = 5

print("a == b?", a == b) 
print("a != b?", a != b) 
print("a > b?", a > b) 
print("a < b?", a < b) 
print("a >= b?", a >= b)  
print("a <= b?", a <= b)   

print("\nOperadores Lógicos:")
print("(a > 5 and b < 10)?", a > 5 and b < 10)
print("(a < 5 or b < 10)?", a < 5 or b < 10) 

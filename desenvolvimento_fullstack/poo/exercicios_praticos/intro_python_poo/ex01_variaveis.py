# EXERCÍCIO 01 – VARIÁVEIS E TIPOS DE DADOS
# Objetivos:
# - Declarar variáveis com diferentes tipos
# - Exibir informações no terminal com print()
# - Usar type() para mostrar o tipo das variáveis
# ============================================

name = "Grazi"
age = 23
height = 1.67
is_studying = True

print("*********** Your Information ***********")
print("Name:", name)
print("Age:", age, "years")
print("Height:", height, "m")
print("Studying?", is_studying)

print("\n*********** Data Types ***********")
print("Type of 'name':", type(name))
print("Type of 'age':", type(age))
print("Type of 'height':", type(height))
print("Type of 'is_studying':", type(is_studying))
